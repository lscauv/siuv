    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        // TODO add your handling code here:
        int tot = 0;
        for (int fila = 0; fila < tProductoV.getRowCount(); fila++) {
            tot = (int) (double) (tot + (Double) tProductoV.getValueAt(fila, 5)); // la columna 2 es la de costo.
            Total.setText(Double.toString(tot));
        }
        double subt = tot*.84;
        double iv = tot*.16;
            SubTotal.setText(Double.toString(subt));
            IVA.setText(Double.toString(iv));
    }//GEN-LAST:event_btnTotalActionPerformed

