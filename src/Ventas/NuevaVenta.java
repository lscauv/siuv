    public String Factura() {
        String FactSql = "SELECT MAX(ID_NFacturas)+1 AS NumeroFacturas FROM ventas";
        try {
            conexion = data.getConnection();
            psExecute = conexion.prepareStatement(FactSql);
            rs = psExecute.executeQuery();
            while (rs.next()) {
                String fac = rs.getString("NumeroFacturas");
                nFactura.setText(fac);
                return fac;
            }
            String resu = FactSql;
            return (resu);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return "return catch";
        }

    }

