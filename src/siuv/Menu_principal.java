     public Menu_principal(String permisos) {
        initComponents();
         if (permisos == null){
            dispose();
        } else {
            perm = permisos; 
            this.setExtendedState(MAXIMIZED_BOTH);
            switch(perm){
                
                case "Admin":
                    break;
                
                case "Supervisor":
                    btnVent.setEnabled(false);
                    break;
                
                case "Vendedor":
                    btnProveedores.setEnabled(false);
                    btnCC.setEnabled(false);
                    btnCP.setEnabled(false);
                    break;
                    
                default:
                    System.exit(0);
                    break;
            }
        }
     }
